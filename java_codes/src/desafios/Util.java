package java_codes.src.desafios;

public class Util {
    public int maiorValor(int a, int b) {
        // if (a > b) {
        //     return a;
        // } else {
        //     return b;
        // }
        return a > b ? a : b; // Operador ternário
    }

    public double maiorValor(double a, double b) {
        // if (a > b) {
        //     return a;
        // } else {
        //     return b;
        // }
        return a > b ? a : b; // Operador ternário
    }

    public String maiorValor(String a, String b) {
        // if (a.length() > b.length()) {
        //     return a;
        // } else {
        //     return b;
        // }
        return a.length() > b.length() ? a : b; // Operador ternário
    }

    
}
